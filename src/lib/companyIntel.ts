export function lookupCompanyIntel(companyName: string): CompanyIntel | null {
  const key = companyName.toLowerCase().replace(/[^a-z0-9\s.]/g, "").trim();
  if (DATA[key]) return DATA[key];

  for (const [k, v] of Object.entries(DATA)) {
    if (key.includes(k) || k.includes(key)) return v;
  }

  const domainGuess = key.replace(/\s+/g, "") + ".com";
  for (const v of Object.values(DATA)) {
    if (v.domain === domainGuess) return v;
  }

  return null;
}

export function lookupByDomain(domain: string): CompanyIntel | null {
  const clean = domain.toLowerCase().trim();
  for (const v of Object.values(DATA)) {
    if (v.domain === clean) return v;
  }
  for (const v of Object.values(DATA)) {
    if (clean.includes(v.domain.split(".")[0])) return v;
  }
  return null;
}
